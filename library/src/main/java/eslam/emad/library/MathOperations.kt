package eslam.emad.library

object MathOperations {

    fun plus(firstNumber: Int, secondNumber: Int): Int {
        return firstNumber + secondNumber
    }

    fun minus(firstNumber: Int, secondNumber: Int): Int {
        return firstNumber - secondNumber
    }

    fun multiply(firstNumber: Int, secondNumber: Int): Int {
        return firstNumber * secondNumber
    }

    fun divide(firstNumber: Int, secondNumber: Int): Int {
        return firstNumber / secondNumber
    }

    fun square(number: Int): Int {
        return number * number
    }
}